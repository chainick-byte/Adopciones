    private void TablaAdoptanteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaAdoptanteMouseClicked
        int fila = TablaAdoptante.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "no se ha seleccionado ninguna fila---");
        } else {
            id_adoptante = Integer.parseInt(TablaAdoptante.getValueAt(fila, 0).toString());
            String nombre = TablaAdoptante.getValueAt(fila, 1).toString();
            String apellido_1 = TablaAdoptante.getValueAt(fila, 2).toString();
            String apellido_2 = TablaAdoptante.getValueAt(fila, 3).toString();
            String direccion = TablaAdoptante.getValueAt(fila, 4).toString();
            String ciudad = TablaAdoptante.getValueAt(fila, 5).toString();
            String comunidad_autonoma = TablaAdoptante.getValueAt(fila, 6).toString();
            int codigo_postal = Integer.parseInt(TablaAdoptante.getValueAt(fila, 7).toString());

            //form.setText(variables de arriba)
            txtNombreAdoptante.setText(nombre);
            txtPrimerPellidoAdoptante.setText(apellido_1);
            txtSegundoPellidoAdoptante.setText(apellido_2);
            txtDireccionAdoptante.setText(direccion);
            txtCiudadAdoptante.setText(ciudad);
            jComboBox1.setSelectedItem(comunidad_autonoma);
            txtCodigoPostalAdoptante.setText("" + codigo_postal);
        }
    }//GEN-LAST:event_TablaAdoptanteMouseClicked

