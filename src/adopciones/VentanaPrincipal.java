    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        if (txtBuscar.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "introduce valor de busqueda");
        }
        switch (cmBoxCategoriaBusqueda.getSelectedItem().toString()) {
            case "SELECCIONA BUSQUEDA":
                JOptionPane.showMessageDialog(this, "no has seleccionado categoria de busqueda");
                break;
            case "NOMBRE PERRO":
                sql = "select * from perro where nombre=?;";
                parametroBusqueda = txtBuscar.getText();
                limpiarTabla(modeloPerroTabla);
                rellenameTablaPerroBuscar(sql, parametroBusqueda);

                break;
            case "RAZA":
                sql = "select * from perro where raza=?;";
                parametroBusqueda = txtBuscar.getText();
                limpiarTabla(modeloPerroTabla);
                rellenameTablaPerroBuscar(sql, parametroBusqueda);

                break;
            case "CIUDAD ADOPTANTE":
                sql = "select * from adoptante where ciudad=?";
                parametroBusqueda = txtBuscar.getText();
                limpiarTabla(modeloAdoptanteTabla);
                rellenameTablaAdoptanteBuscar(sql, parametroBusqueda);

                break;
            case "CODIGO POSTAL ADOPTANTE":
                sql = "select * from adoptante where codigo_postal=?";
                parametroBusqueda = txtBuscar.getText();
                limpiarTabla(modeloAdoptanteTabla);
                rellenameTablaAdoptanteBuscar(sql, parametroBusqueda);

                break;
            case "COMUNIDAD AUTONOMA ADOPTANTE":
                sql = "select * from adoptante where comunidad_autonoma=?";
                parametroBusqueda = txtBuscar.getText();
                limpiarTabla(modeloAdoptanteTabla);
                rellenameTablaAdoptanteBuscar(sql, parametroBusqueda);
                break;
            default:
                break;
        }
    }//GEN-LAST:event_jButton8ActionPerformed

